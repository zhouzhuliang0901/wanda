package com.wondersgroup.infopub.bean;

public class SetInfopubDevicePublish {
	/**
	 * 设备信息
	 */
	private InfopubDeviceInfo infopubDeviceInfo;
	/**
	 * 发布信息
	 */
	private InfopubPublishExt infopubPublishExt;
	/**
	 * @return the infopubDeviceInfo
	 */
	public InfopubDeviceInfo getInfopubDeviceInfo() {
		return infopubDeviceInfo;
	}
	/**
	 * @param infopubDeviceInfo the infopubDeviceInfo to set
	 */
	public void setInfopubDeviceInfo(InfopubDeviceInfo infopubDeviceInfo) {
		this.infopubDeviceInfo = infopubDeviceInfo;
	}
	/**
	 * @return the infopubPublishExt
	 */
	public InfopubPublishExt getInfopubPublishExt() {
		return infopubPublishExt;
	}
	/**
	 * @param infopubPublishExt the infopubPublishExt to set
	 */
	public void setInfopubPublishExt(InfopubPublishExt infopubPublishExt) {
		this.infopubPublishExt = infopubPublishExt;
	}
}
