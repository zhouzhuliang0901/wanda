//模糊查询
function fuzzyQuery(list, name, key) {　　
	var result = [];　　
	list.map(function(item, index) {　　
		if(item[key].match(name)) {　　
			result.push(item);　　
		}
	});
	return result;
}
//根据节点key得到对应信息 
function filterByInfo(dataJsonp, condition, key) {
	var result;
	for(var i = 0; i < dataJsonp.length; i++) {
		if(dataJsonp[i][key] == condition) {
			result = dataJsonp[i];
		}
	}
	if(result) {
		return result.img;
	} else {
		return result;
	}
}

//筛选
function screen(dayweather) {
	let dayWeather = dayweather;
	let weatherIndex = -1;
	// 最优先天气
	let weather;
	for(var i = 0; i < weatherList.length; i++) {
		if(dayWeather.indexOf(weatherList[i].name) >= 0 && (weatherIndex == -1 || dayWeather.indexOf(weatherList[i].name) < weatherIndex)) {
			weatherIndex = dayWeather.indexOf(weatherList[i].name);
			weather = weatherList[i];
		}
	}
	return weather.img;
}
